    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
         String maPT = txtMaPT.getText();
        String tenPT = txtTenPT.getText();
        String diachi = txtDiaChi.getText();
        String sochongoi = txtSoChoNgoi.getText();
        String soDT = txtSoDienThoai.getText();
        int chiphi = Integer.parseInt(txtChiPhi.getText());
        
        PhuongTienDTO ptDTO = new PhuongTienDTO(maPT, tenPT, diachi, chiphi, soDT, chiphi);
        PhuongTienBUS.CapNhatTTPT(ptDTO);
        
        int i = tblPhuongTien.getSelectedRow();
        PhuongTienBUS.XemDSPT();
        tableModel.setValueAt(maPT, i, 0);
        tableModel.setValueAt(tenPT, i, 1);
        tableModel.setValueAt(diachi, i, 2);
        tableModel.setValueAt(sochongoi, i, 3);
        tableModel.setValueAt(soDT, i, 4);
        tableModel.setValueAt(chiphi, i, 5);
        tblPhuongTien.setModel(tableModel);

    }//GEN-LAST:event_btnCapNhatActionPerformed

