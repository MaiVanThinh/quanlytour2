    private void showPhuongTien() {
        phuongtienList = PhuongTienBUS.XemDSPT();
        tableModel.setRowCount(0);
        phuongtienList.forEach(ptDTO -> {
            tableModel.addRow(new Object[]{
                ptDTO.getMaPT(), ptDTO.getTenPT(), ptDTO.getDiachi(), ptDTO.getSochongoi(), ptDTO.getSoDT(), ptDTO.getChiphi()
            });
        });
    }

    private void showData() {
        int selectedIndex = tblPhuongTien.getSelectedRow();

        if (selectedIndex >= 0) {
            PhuongTienDTO ptDTO = phuongtienList.get(selectedIndex);

            txtMaPT.setText(ptDTO.getMaPT());
            txtTenPT.setText(ptDTO.getTenPT());
            txtDiaChi.setText(ptDTO.getDiachi());
            txtSoChoNgoi.setText(Integer.toString(ptDTO.getSochongoi()));
            txtSoDienThoai.setText(ptDTO.getSoDT());
            txtChiPhi.setText(Integer.toString(ptDTO.getChiphi()));

        }
    }

