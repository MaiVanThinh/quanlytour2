    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Mời nhập mã địa điểm !");

            if (input != null && input.length() > 0) {
                cttcList = CTThuChiBUS.findByMaCT(input);

                tableModel.setRowCount(0);

                cttcList.forEach(cttcDTO -> {
                    tableModel.addRow(new Object[]{
                        cttcDTO.getMaCTTC(), cttcDTO.getMaCTKH(), cttcDTO.getTongThu(), cttcDTO.getTongChi(),cttcDTO.getDoanhThu()
                    });
                });
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi tìm kiếm !");
            }
    }//GEN-LAST:event_btnTimKiemActionPerformed

