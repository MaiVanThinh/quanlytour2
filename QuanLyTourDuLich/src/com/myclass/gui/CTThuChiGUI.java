    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblCTTC.getSelectedRow();
        if (selectedIndex >= 0) {
            CTThuChiDTO cttcDTO = cttcList.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn xóa chi tiết thu chi này ?");
            //System.out.println("option: " + option);
            //yes->0
            //No->1
            //Cancel->2
            if (option == 0) {
                CTThuChiBUS.XoaCTTC(cttcDTO.getMaCTTC());
                showCTTC();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

