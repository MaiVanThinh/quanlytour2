    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        String MaCTThuChi = txtmaCTTC.getText();
        String MaCTKeHoach = txtmaCTKH.getText();
        int TongThu = Integer.parseInt(txttongthu.getText());
        int TongChi = Integer.parseInt(txttongchi.getText());
        int DoanhThu = Integer.parseInt(txtdoanhthu.getText());

        CTThuChiDTO cttcDTO = new CTThuChiDTO(MaCTThuChi, MaCTKeHoach, TongChi, TongThu, DoanhThu);

        CTThuChiBUS.CapNhatCTTC(cttcDTO);
        //showKhachSan();

        int i = tblCTTC.getSelectedRow();
        CTThuChiBUS.xemDSCT();
        tableModel.setValueAt(MaCTThuChi, i, 0);
        tableModel.setValueAt(MaCTKeHoach, i, 1);
        tableModel.setValueAt(TongChi, i, 2);
        tableModel.setValueAt(TongThu, i, 3);
        tableModel.setValueAt(DoanhThu, i, 4);

        tblCTTC.setModel(tableModel);
    }//GEN-LAST:event_btnCapNhatActionPerformed

