    private void showCTTC() {
        cttcList = CTThuChiBUS.xemDSCT();
        tableModel.setRowCount(0);

        cttcList.forEach(cttcDTO -> {
            tableModel.addRow(new Object[]{
                cttcDTO.getMaCTTC(), cttcDTO.getMaCTKH(), cttcDTO.getTongThu(), cttcDTO.getTongChi(), cttcDTO.getDoanhThu()

            });
        });
    }

    private void showData() {
        int selectedIndex = tblCTTC.getSelectedRow();
        if (selectedIndex >= 0) {
            CTThuChiDTO cttcDTO = cttcList.get(selectedIndex);

            txtmaCTTC.setText(cttcDTO.getMaCTTC());
            txtmaCTKH.setText(cttcDTO.getMaCTKH());
            txttongthu.setText(Integer.toString(cttcDTO.getTongThu()));
            txttongchi.setText(Integer.toString(cttcDTO.getTongChi()));
            txtdoanhthu.setText(Integer.toString(cttcDTO.getDoanhThu()));
        }
    }

